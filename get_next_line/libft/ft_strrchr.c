#include "libft.h"

char		*ft_strrchr(const char *s, int c)
{
	char	*s1;

	s1 = (char *)s + ft_strlen(s);
	while (*s1 != c)
	{
		if (s1 == s)
			return (NULL);
		s1--;
	}
	return (s1);
}
